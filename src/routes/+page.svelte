<script>
    import { enhance } from '$app/forms';
    import { onMount } from 'svelte';

    export let form;
    export let data;

    let objectQueue = [];
    let currentObject = null;
    let isLoading = false;
    let objectImage = null;
    let photographer = null;
    let imagePageUrl = null;
    let showDescription = false;

    function addObjectsToQueue(objects) {
        objectQueue = [...objectQueue, ...objects];
    }

    function getNextObject() {
        if (objectQueue.length > 0) {
            currentObject = objectQueue.shift();
            if (objectQueue.length === 2) {
                requestNewObjects();
            }
        }
    }

    function requestNewObjects() {
        if (!isLoading) {
            isLoading = true;
            document.getElementById('generateForm').requestSubmit();
        }
    }

    $: if (data.randomObjects && objectQueue.length === 0) {
        addObjectsToQueue(data.randomObjects);
        getNextObject();
    }

    $: if (form?.randomObjects) {
        addObjectsToQueue(form.randomObjects);
    }

    function handleObjectClick(event) {
        // Evitar que se genere un nuevo objeto si se hace clic en el botÃ³n de descripciÃ³n
        if (!event.target.closest('.description-toggle')) {
            getNextObject();
            showDescription = false;
        }
    }

    onMount(() => {
        if (objectQueue.length === 0) {
            requestNewObjects();
        }
    });

    function handleSubmit() {
        return async ({ result }) => {
            if (result.type === 'success') {
                addObjectsToQueue(result.data.randomObjects);
            }
            isLoading = false;
        };
    }

    async function fetchObjectImage(objectName) {
        try {
            const response = await fetch(`/api/image-search?q=${encodeURIComponent(objectName)}`);
            const data = await response.json();
            if (data.imageUrl) {
                objectImage = data.imageUrl;
                photographer = data.photographer;
                imagePageUrl = data.pageUrl;
            } else {
                objectImage = null;
                photographer = null;
                imagePageUrl = null;
            }
        } catch (error) {
            console.error('Error fetching image:', error);
            objectImage = null;
            photographer = null;
            imagePageUrl = null;
        }
    }

    $: if (currentObject) {
        fetchObjectImage(currentObject.name);
    }

    function toggleDescription() {
        showDescription = !showDescription;
    }
</script>

